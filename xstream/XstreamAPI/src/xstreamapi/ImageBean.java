package xstreamapi;

/**
 *
 * @author jconner
 */
public class ImageBean {
    private String url;
    private String title;
    private String link;
    
    public ImageBean() {
        
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }
    
}
